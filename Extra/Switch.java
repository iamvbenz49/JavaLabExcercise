package Extra;

public class Switch {
    public static void main(String[] args) {
        switch (14 % 4) {
        case 1:
          System.out.println("www.includehelp.com");
          break;
        case 2:
          System.out.println("www.fb.com");
        case 3:
          System.out.println("www.google.com");
          break;
        default:
          System.out.println("wrong choice");
          break;
        }
    }
}
