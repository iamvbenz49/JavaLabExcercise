package Extra;

public class Switch2 {
    public static void main(String[] args) {
        String STR = "hello";
      switch (STR) {
        case "HELLO":
          System.out.println("@@@@@@@@@@@@@@@");
          break;
        case "hello":
          System.out.println("$$$$$$$$$$$$$$$");
          break;
        case "STR":
          System.out.println("!!!!!!!!!!!!!!!!!!!!!!!!");
          break;
        default:
          System.out.println("###############");
          break;
        }
      }
}
